func drawText(dst *ebiten.Image, s string, x, y, scale float32, c color.Color) {
	x0 := x
	for _, r := range strings.ToUpper(s) {
		if r == '\n' {
			y += 9 * scale
			x = x0
			continue
		}
		g, ok := glyph[r]
		if !ok {
			g = glyph[' ']
		}
		for yy, row := range g {
			for xx, b := range row {
				if b == '1' {
					vector.DrawFilledRect(dst, x+float32(xx)*scale, y+float32(yy)*scale, scale, scale, c, false)
				}
			}
		}
		x += 6 * scale
	}
}
func textWidth(s string, scale float32) float32 {
	max, n := 0, 0
	for _, r := range s {
		if r == '\n' {
			if n > max {
				max = n
			}
			n = 0
		} else {
			n++
		}
	}
	if n > max {
		max = n
	}
	return float32(max) * 6 * scale
}
